# Caluclation

## ** (乗数計算)
p 4 ** 2 #==> 16

## / (割り算)
p 4 / 2 #=> 2

## div (割り算)
p 4.div(2) #=> 2

## quo (割り算)答えを分数で返す
p 4.quo(2) #=> (2/1)

## % (余りの計算)
p 4 % 2 #=> 0

## modulo (余りの計算)
p 4.modulo(2) #=> 0

## chr (数字に対応する文字列を返す)
p 65.chr #=> "A"

## ord (文字列に対応する数字を返す)
p "A".ord #=> 65

## next (次の数字を返す)
p 1.next #=> 2

## succ (次の数字を返す)
p 1.succ #=> 2

## pred (前の数字を返す)
p 1.pred #=> 0
